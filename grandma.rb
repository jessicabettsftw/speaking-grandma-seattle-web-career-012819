# Write a speak_to_grandma method.
def speak_to_grandma(input)

  if input == input.upcase
    puts "NO, NOT SINCE 1938!"
  end
  elsif input == "I LOVE YOU GRANDMA!"
    puts "I LOVE YOU TOO PUMPKIN!"
  end
  else
    puts "HUH?! SPEAK UP, SONNY!"
  end
end
